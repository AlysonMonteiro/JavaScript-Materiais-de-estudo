/*
operadores de comparação
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
== igualdade
=== igualdade estrita
!= diferente
!== diferente estrito
*/

const maiorQue =  10 > 5 ;
const maiorQueOuIgualA = 10 >= 5;
const menorQue = 10 < 5;
const menorQueOuIgualA = 10 <= 5;
const igualdade = 10 == 10;
const igualdadeRestrital = 10 === 10;
const diferente = 10 != '10';
const diferenteRestrito = 10 !== '10';

console.log(
    maiorQue,
    maiorQueOuIgualA,
    menorQue,
    menorQueOuIgualA,
    igualdade,
    igualdadeRestrital,
    diferente,
    diferenteRestrito
    );